def get_random_date():
    start = datetime.strptime('1/1/2021 12:30 PM', '%m/%d/%Y %I:%M %p')
    end = datetime.strptime('1/12/2023 4:50 AM', '%m/%d/%Y %I:%M %p')

    delta = end - start
    int_delta = (delta.days * 24 * 60 * 60) + delta.seconds
    random_second = random.randrange(int_delta)
    return start + timedelta(seconds=random_second)
